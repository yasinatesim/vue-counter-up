<template>
  <div class="card">
    <div class="card-body">
      <h2 class="title">Counter Up App</h2>
      <div class="card-content">
        <button class="btn btn--gray" @click="decrease">-</button>
        <span class="counter">{{ counter }}</span>
        <button class="btn btn--red" @click="increase">+</button>
      </div>
      <div>
        <button class="btn btn--gradient" @click="reset">Reset</button>
      </div>
    </div>
    <div class="circle circle--small"></div>
    <div class="circle circle--big"></div>
  </div>
</template>

<script>
export default {
  name: "CounterUp",
  data() {
    return {
      counter: 0
    };
  },
  methods: {
    increase() {
      this.counter++;
    },

    decrease() {
      this.counter--;
    },

    reset() {
      this.counter = 0;
    }
  }
};
</script>

